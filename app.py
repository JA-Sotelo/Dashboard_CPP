# ── HELPERS ───────────────────────────────────────────────────────────────────
def clean(v) -> str:
    """Elimina NaN/None/nat que pandas convierte a string con dtype=str."""
    if v is None:
        return ""
    s = str(v).strip()
    if s.lower() in ("nan", "none", "nat", ""):
        return ""
    return s


def normalizar_col(s: str) -> str:
    import unicodedata
    s = unicodedata.normalize("NFD", str(s))
    s = "".join(c for c in s if unicodedata.category(c) != "Mn")
    return s.lower().replace(" ", "").replace("_", "")


# ── PROCESAMIENTO EXCEL ───────────────────────────────────────────────────────
def procesar_excel(archivo) -> list:
    df = pd.read_excel(archivo, dtype=str)
    df.columns = [str(c).strip() for c in df.columns]

    col_fecha = None
    for c in df.columns:
        n = normalizar_col(c)
        if any(p in n for p in ["fecha", "date", "fec", "ingreso"]):
            col_fecha = c
            break

    rows = []
    for _, row in df.iterrows():
        obj = {col: clean(row[col]) for col in df.columns}

        if col_fecha and obj.get(col_fecha):
            try:
                dt = pd.to_datetime(obj[col_fecha], dayfirst=True, errors="coerce")
                if pd.notna(dt):
                    obj[col_fecha]     = dt.strftime("%Y-%m-%d")
                    obj["__PERIODO__"] = f"{MO_MAP[dt.month]}-{str(dt.year)[2:]}"
                    obj["__ANIO__"]    = str(dt.year)
                else:
                    obj["__PERIODO__"] = ""
                    obj["__ANIO__"]    = ""
            except Exception:
                obj["__PERIODO__"] = ""
                obj["__ANIO__"]    = ""
        else:
            obj["__PERIODO__"] = ""
            obj["__ANIO__"]    = ""

        rows.append(obj)

    return rows


# ── GENERACION HTML ───────────────────────────────────────────────────────────
def generar_html(rows: list, password: str, filename: str) -> str:
    html = get_html_template()

    # ensure_ascii=True: OBLIGATORIO — caracteres no-ASCII rompen el JS en runtime
    json_data = json.dumps(rows, ensure_ascii=True, separators=(",", ":"))

    html = html.replace(
        "let RAW = [], COLS = [], CHARTS = {}, PAGE = 1;",
        f"let RAW = {json_data}, COLS = [], CHARTS = {{}}, PAGE = 1;"
    )
    html = html.replace(
        "const DASHBOARD_PASSWORD = 'pap2025';",
        f"const DASHBOARD_PASSWORD = {json.dumps(password)};"
    )
    html = html.replace(
        "setTimeout(() => { overlay.style.display = 'none'; }, 460);",
        "setTimeout(() => { overlay.style.display = 'none'; initDashboard(); }, 460);"
    )
    html = html.replace(
        "Sin datos · cargá un archivo para comenzar",
        f"Datos de: {filename}"
    )
    return html


